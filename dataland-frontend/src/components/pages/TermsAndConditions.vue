      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        3.4.4  Dataland haftet zudem für den Verlust von Daten jedenfalls nur bis zu dem Betrag, der bei ordnungsgemäßer
        und regelmäßiger Sicherung der Daten zu deren Wiederherstellung angefallen wäre.
