        &nbsp;
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        4.1.4 Dataland hat das Recht, die von dem Daten-Sammler eingegebenen Unternehmens-Daten nach eigenen und im
        ausschließlich eigenen Ermessen von Dataland liegenden Maßgaben zu überprüfen. Auf Grundlage dieser Prüfung
        entscheidet Dataland, ob die vom Daten-Sammler gelieferten Unternehmens-Daten als Unternehmens-Daten der
        Plattform-Dataland für den Zugriff von Daten-User zugelassen werden.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        &nbsp;
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        4.1.5 Auf diese Weise zugelassene Unternehmens-Daten werden auf der Plattform-Dataland für Daten-User
        bereitgestellt. Solange sie für Daten-User auf der Plattform-Dataland zur Verfügung stehen, dienen die
        Unternehmens-Daten als Berechnungsgrundlage für den Vergütungsanspruch des Daten-Sammlers.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        &nbsp;
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        4.1.6 Dataland unterrichtet den Daten-Sammler, wenn Unternehmens-Daten nicht zugelassen werden. Dataland ist
        nicht verpflichtet ist, die Gründe für die verweigerte Zulassung anzugeben. Es besteht kein Vergütungsanspruch
        für nicht zugelassene oder später wieder entfernte Unternehmens-Daten.
