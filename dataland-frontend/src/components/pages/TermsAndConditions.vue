        als Unternehmens-Daten auf der Plattform-Dataland für den Zugriff von Daten-Usern belassen werden oder ob der
        Zugriff auf diese gesperrt wird. Im Falle einer Sperrung oder Entfernung der Unternehmens-Daten gilt § 4.1.6
