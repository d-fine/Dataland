      <p class="MsoNormal" style="margin-left: 1cm; text-align: justify; text-indent: -1cm">
        <b>4.1      Übertragung der Daten an Dataland durch den Daten-Sammler</b>
      </p>

      <p class="MsoListParagraphCxSpFirst" style="margin-left: 54pt; text-align: justify; text-indent: -25.65pt">
        &nbsp;
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        4.1.1 Der Daten-Sammler trägt Unternehmens-Daten über ein Datenobjekt zusammen. Er fügt die Unternehmens-Daten
        in Bezug auf ein Datenobjekt in die Datenmatrix ein, die Dataland auf der Plattform-Dataland zu diesem Zwecke
        vorhält.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        &nbsp;
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        4.1.2 Die Unternehmens-Daten werden vom Daten-Sammler in Bezug auf einzelne Unternehmens-Thematiken bzw. -Fragen
        zur Verfügung gestellt. Eine solche einzelne Einheit wird als Datenpunkt bezeichnet.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        &nbsp;
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -25.65pt">
        4.1.3 Der Daten-Sammler kann Unternehmens-Daten für ein Datenobjekt an Dataland übertragen, zu dem es bislang
        keine Unternehmens-Daten gab. Er kann auch bestehende Datensätze für ein Datenobjekt mit noch fehlenden Daten
        ergänzen oder durch jüngere Unternehmens-Daten aktualisieren.
