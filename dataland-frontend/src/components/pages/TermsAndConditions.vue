        Dataland auch im Hinblick auf die datenschutzrechtlichen Gesetze und rechtlichen Vorgaben uneingeschränkt und
        insbesondere ohne dahingehende Einwilligungen oder Genehmigungen einzuholen oder ohne sich auf gesetzliche
        Erlaubnistatbestände berufen zu müssen gemäß § 3.1 auf der Plattform-Dataland den Daten-Usern zur dort
        beschriebenen Nutzung zur Verfügung stellen kann.
      </p>

      <p class="MsoListParagraphCxSpLast" style="margin-left: 70.9pt; text-align: justify">&nbsp;</p>

      <p class="MsoNormal" style="margin-left: 1cm; text-align: justify; text-indent: -1cm">
        <b>4.4      Die Vergütungsansprüche des Daten-Sammlers </b>
      </p>

      <p class="MsoListParagraphCxSpFirst" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        4.4.1  Ausgangspunkt der Vergütung des Daten-Sammlers sind die Unternehmens-Daten, die der Daten-Sammler in
        Bezug auf die einzelnen Unternehmens-Thematiken Dataland zur Verfügung gestellt hat und die infolgedessen von
        Dataland in die Plattform-Dataland aufgenommen wurden und die den Daten-Usern zur Verfügung stehen. Auf den
        tatsächlichen Abruf der entsprechenden Informationen durch Daten-User<span class="msoDel"
          ><del cite="mailto:SKW" datetime="2023-04-28T18:54">s</del></span
