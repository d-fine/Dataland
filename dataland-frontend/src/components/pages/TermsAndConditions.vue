      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        3.4.5  Dataland haftet jedenfalls, d.h. unbeschadet anderer Regelungen in diesen AGB-Dataland, unbeschränkt für
        vorsätzlich oder grob fahrlässig verursachte Schäden sowie für Schäden aus der Verletzung des Lebens, des
        Körpers oder der Gesundheit durch Dataland, seine gesetzlichen Vertreter oder Erfüllungsgehilfen.
