        entsprechende gesonderte rechtsgeschäftliche Erklärungen und tatsächliche Handlungen herbeizuführen und zu
        bewirken.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        4.2.3  Der Daten-Sammler garantiert, nur solche Unternehmens-Daten an Dataland zu übertragen, die frei von
        Rechten Dritter sind und bei denen weder vertragliche noch gesetzliche Vorgaben einer tatsächlichen Übertragung
        nach § 4.1 oder der rechtswirksamen Rechteübertragung nach diesem § 4.2 entgegenstehen.
