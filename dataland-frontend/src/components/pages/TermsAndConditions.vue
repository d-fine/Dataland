        <b>4.2         Einräumung der Nutzungsrechte an Dataland seitens des Daten-Sammlers</b>
      </p>

      <p class="MsoListParagraphCxSpFirst" style="margin-left: 54pt; text-align: justify">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        4.2.1  Mit Absenden der Unternehmens-Daten auf der Plattform-Dataland überträgt der Daten-Sammler an Dataland
        ein Nutzungsrecht in der Form, dass Dataland an sämtlichen Daten sowie auch an der Gesamtheit der Daten, und
        zwar auch wenn diese eine Datenbank von § 87a Urhebergesetz bilden sollten, ein unwiderrufliches, räumlich und
        inhaltlich unbeschränktes, zeitlich unbefristetes, übertragbares und unterlizenzierbares Nutzungsrecht erhält.
        Dies umfasst insbesondere das nicht entziehbare und nicht einschränkbare Recht, diese Unternehmens-Daten in
        jeder denkbaren Art und Weise, insbesondere auch im Rahmen jedweder unternehmerischen Betätigung, zu nutzen und
        Dritten zur Verfügung zu stellen oder zu überlassen, so dass auch diese Dritten über entsprechende Rechte
        verfügen. Ebenso hat Dataland das uneingeschränkte Recht, einzelne Unternehmens-Daten, die Daten in ihrer
        Gesamtheit sowie auch etwaige Datenbanken insgesamt zu verändern und/oder zu verarbeiten.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

