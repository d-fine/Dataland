      <p class="MsoNormal" style="text-align: justify"><b>3.4         Haftung von Dataland gegenüber Daten-Usern</b></p>

      <p class="MsoListParagraphCxSpFirst" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        3.4.1  Dataland haftet gegenüber dem Daten-User nur nach Maßgabe der folgenden Bestimmungen und nur soweit die
        Unternehmens-Daten von dem Daten-User im Rahmen der ihm eingeräumten Nutzungsrechte unter Befolgung der dem
        Daten-User obliegenden eigenen Vertragspflichten genutzt und eingesetzt werden.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        3.4.2  Dataland haftet für leicht fahrlässig verursachte Schäden dann, wenn die Dataland zuzurechnende
        Verletzungshandlung eine Verletzung von Kardinalpflichten darstellt, wobei es sich bei Kardinalpflichten um die
        wesentlichen Pflichten handelt, die die Grundlage der Nutzung der Plattform-Dataland und der Unternehmens-Daten
        bilden, die entscheidend für die Nutzung der Plattform-Dataland und der Unternehmens-Daten waren und auf deren
        Erfüllung der Daten-User vertrauen darf.
      </p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">&nbsp;</p>

      <p class="MsoListParagraphCxSpMiddle" style="margin-left: 2cm; text-align: justify; text-indent: -1cm">
        3.4.3  Sollte Dataland Kardinalpflichten nur leicht fahrlässig verletzt haben, ist die Haftung von Dataland auf
        vorhersehbaren Schaden begrenzt.
